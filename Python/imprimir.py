nombre = 'Brandon'
apellido = 'León'

#Por defecto la función print separa los valores que le pasemos con un espacio. Si queremos que separe los valores con otros caracteres diferentes usamos el parámetro sep
print(nombre, apellido, 'GhostBull')
print(nombre, apellido, 'GhostBull', 'edad=%s' %(5*4), sep='-')